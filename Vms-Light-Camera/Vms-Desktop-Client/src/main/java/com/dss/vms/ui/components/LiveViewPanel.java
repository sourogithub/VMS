package com.dss.vms.ui.components;

import java.awt.Dimension;

import javax.swing.JPanel;

/**
 * @author Sibendu
 */
public class LiveViewPanel extends JPanel {

	private static final long serialVersionUID = 1L;
	private static final int DEFAULT_VIEWPANEL_HEIGHT = 700;
	private static final int DEFAULT_VIEWPANEL_WIDTH = 800;

	public LiveViewPanel() {
		setMinimumSize(new Dimension(DEFAULT_VIEWPANEL_WIDTH, DEFAULT_VIEWPANEL_HEIGHT));
		setPreferredSize(new Dimension(DEFAULT_VIEWPANEL_WIDTH, DEFAULT_VIEWPANEL_HEIGHT));
		setMaximumSize(new Dimension(DEFAULT_VIEWPANEL_WIDTH, DEFAULT_VIEWPANEL_HEIGHT));
	}

	/**
	 * @return DEFAULT_VIEWPANEL_HEIGHT
	 */
	public static int getDefaultviewpanelheight() {
		return DEFAULT_VIEWPANEL_HEIGHT;
	}

	/**
	 * @return DEFAULT_VIEWPANEL_WIDTH
	 */
	public static int getDefaultviewpanelwidth() {
		return DEFAULT_VIEWPANEL_WIDTH;
	}

}
